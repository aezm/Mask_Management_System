package model;

public class Mask {

	private int mask_id;
	private String size;
	private String type;
	
	public int getMask_id() {
		return mask_id;
	}
	public void setMask_id(int mask_id) {
		this.mask_id = mask_id;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
}
